package projava;

public class SwitchSample {
    public static void main(String[] args) {
        var a = 3;
        if (a == 1 || a == 2) {
            System.out.println("one-two");
        } else if (a == 3) {
            System.out.println("three");
        } else {
            System.out.println("four");
        }
    }
}
